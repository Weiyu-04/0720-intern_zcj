# 明确的"无行业信号/易误导"名号词，命中且无其他行业词时提示按描述定
_NOISE = ["实业", "集团", "发展", "科技", "投资", "控股", "管理", "企业", "商务", "国际", "环保", "新能源"]

# 语境降级词：与行业词共现时，说明该行业词多半指"经营/服务的对象"而非"本单位主营"。
# 如"建材城"卖建材≠生产建材(30)、"食品销售"≠食品制造(14)、"建筑设计院"≠房屋建筑(47)。
# 命中后只把 high 降为 mid（强制进入"待核实"），不删候选——只会更保守，不会漏掉正确答案。
_CONTEXT_NEG = ["城", "市场", "商城", "广场", "批发", "零售", "销售", "经销", "代理", "贸易",
                "租赁", "设计", "研究院", "研究所", "设计院", "咨询", "检测", "维修", "物业"]


def classify(name: str) -> dict:
    name = (name or "").strip()
    if not name:
        return {"ok": False, "error": "单位名称为空。"}

    # 登记类型（取最先命中的最具体后缀）
    org = None
    for kw, val in _ORG_RULES:
        if kw in name:
            org = {"code": val[0], "name": val[1], "matched": kw,
                   "note": "仅判组织形式；私营/国有/内外资等经济性质名字通常判不出，待核实"}
            break

    # 行业候选（收集全部命中，按 管理分类+大类 去重：同一管理分类下的不同大类都要保留，
    # 否则"食品饮料公司"只剩食品14、丢掉饮料15，与"把候选都列出来"的设计意图相悖）
    ctx = [w for w in _CONTEXT_NEG if w in name]
    seen, inds = set(), []
    for kw, mgmt, dalei, dname, conf, note in _IND_RULES:
        key = (mgmt, dalei)
        if kw in name and key not in seen:
            seen.add(key)
            if ctx and conf == "high":  # 语境降级：名中有"城/销售/租赁…"，行业词可能指经营对象
                conf = "mid"
                note = (note + "；" if note else "") + f"名中含“{'、'.join(ctx)}”，“{kw}”可能指经营/服务对象而非本单位主营，须按事故经过核实"
            inds.append({"管理分类": mgmt, "大类": dalei, "大类名": dname,
                         "matched": kw, "confidence": conf, "note": note})

    noise = [w for w in _NOISE if w in name]
    need_check = (not inds) or any(i["confidence"] != "high" for i in inds) or len(inds) > 1

    return {
        "ok": True,
        "name": name,
        "登记类型候选": org,
        "行业候选": inds,
        "名号噪声词": noise,
        "语境降级词": ctx,
        "待核实": need_check,
        "提示": ("行业候选仅据公司名（启发式）。请以'事故发生单位在事故中的作业/主营活动描述'为主判据交叉核对；"
                 "名字无信号、含噪声词或多候选/低置信时，按发生单位主营+附录二对照表口径判定并标待核实。"),
    }


def _selftest():
    r = classify("某某货运有限公司")
    assert r["登记类型候选"]["name"] == "有限责任公司"
    assert any(i["大类"] == "54" for i in r["行业候选"])
    r2 = classify("某某新能源科技有限公司")
    assert r2["待核实"] and "新能源" in r2["名号噪声词"]  # 名字判不出行业，应待核实
    r3 = classify("某某实业有限公司")
    assert r3["待核实"] and not r3["行业候选"] and "实业" in r3["名号噪声词"]
    r4 = classify("山西某某煤业有限公司")
    assert any(i["大类"] == "06" for i in r4["行业候选"])
    r5 = classify("某某食品股份有限公司")
    assert r5["登记类型候选"]["name"] == "股份有限公司" and any(i["大类"] == "14" for i in r5["行业候选"])
    # 语境降级：卖建材≠生产建材，不得给 high/免核实
    r6 = classify("某某建材城经营管理有限公司")
    assert r6["待核实"] and r6["语境降级词"] == ["城"]
    assert all(i["confidence"] != "high" for i in r6["行业候选"])
    assert any(i["大类"] == "30" for i in r6["行业候选"])  # 候选仍保留，只是不再断言
    for n in ("某某家具城有限公司", "某某建筑设计研究院有限公司", "某某食品销售有限公司", "某某机械租赁有限公司"):
        assert classify(n)["待核实"], n
    # 无语境词的正常名字不受影响，仍是 high
    assert not classify("某某食品股份有限公司")["语境降级词"]
    assert any(i["confidence"] == "high" for i in classify("山西某某煤业有限公司")["行业候选"])
    # 去重键含大类：同一管理分类下的不同大类都要留住
    r7 = classify("某某食品饮料有限公司")
    assert {"14", "15"} <= {i["大类"] for i in r7["行业候选"]}
    r8 = classify("某某纺织服装有限公司")
    assert {"17", "18"} <= {i["大类"] for i in r8["行业候选"]}
    print("selftest OK")


